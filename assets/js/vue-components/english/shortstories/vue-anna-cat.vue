The cat, whose name was Gareth by the way, would be given instructions by his manager on how to proceed with the following difficult matter:
Unhappily, Gareth accepted these intructions because he needed to keep his job and earn a living to be able to support his nine small kittens and his beautiful cat wife. However, the matter was really delicate and difficult so he had to work a lot.
Time to explain this in a bit more detail. The cat worked as a detective.
A female customer had consulted their detective company, because she had no trust in her husband anymore and wanted them to survey her husband, check where he goes, who he meets, and so on...
Genuinely speaking, hard work!
Despite his strict manager and the hard work, Gareth loved his job and he was committed to survey this cat to his best abilities. So when his manager assigned him the mission, he said:
 "I will do my best, sir!"
Gareth had already followed the husband of that jealous wife until the restroom.
That the husband had stayed there for 2 hours was indeed suspicious. A business like that doesn't take that long! So further inquiries were necessary.
Gareth stayed nearby the restroom door and while watching the door, he started to come up with ideas for this strange behaviour. First, he thought that this cat had an affaire but during the two hours, there was no female cat going in or out. So he had to think more...
Suddenly, a deafening sound came out of the cabin. Was this the sound of a non-well-digested piece of meat?
Gareth wasn't sure but he had no choice than to stay and keep waiting. When his client's husband finally showed up from the cabin, he smiled at Gareth, not knowing that Gareth was there to spy on him.
"What have you done in that cabin for 2 hours?" Gareth wanted to know. Although detectives usually don't act like that, Gareth chose fo this direct option because he wanted to finish the job quickly and spend time with his family.
"I think my wife packed me some expired food for lunch. You know, we don't really have a good marriage. Sometimes I think she wants to poison me. But luckily, I'm feeling alright now, answered the suspicious cat.

Not being sure whether Gareth should believe him or not, he just nodded his head as an answer.
"If I knew what else your wife does wrong, I would be able to help you by talking to her. You probably don't know, that your wife consulted the detective company I work for and paid a lot of money in order to have you tracked down."
Gareth knew that he had just broken his promise to stay in the shadows as a detective by declaring who he was to this cat, but suddenly he had a strange feeling that maybe it was the wife who was doing something bad.
"I will try to sum up what my wife does wrong: First of all, she never shuts up and always talks although I desperately need silence. Secondly, she gets up too early and wakes me up too while doing it. And last but not least, she never finishes a project but always gives up once a small obstacle gets in her way. Observing that demotivates me."
